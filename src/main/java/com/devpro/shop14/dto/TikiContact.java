package com.devpro.shop14.dto;

public class TikiContact {
	private String txtmainsp;
	private String txtdetailsp;
	private String txtEmail;
	private String txtPhone;	
	private String txtID;
	private String txtTitle;
	private String txtContent;
	
	
	public String getTxtmainsp() {
		return txtmainsp;
	}
	public void setTxtmainsp(String txtmainsp) {
		this.txtmainsp = txtmainsp;
	}
	public String getTxtdetailsp() {
		return txtdetailsp;
	}
	public void setTxtdetailsp(String txtdetailsp) {
		this.txtdetailsp = txtdetailsp;
	}
	public String getTxtEmail() {
		return txtEmail;
	}
	public void setTxtEmail(String txtEmail) {
		this.txtEmail = txtEmail;
	}
	public String getTxtPhone() {
		return txtPhone;
	}
	public void setTxtPhone(String txtPhone) {
		this.txtPhone = txtPhone;
	}
	public String getTxtID() {
		return txtID;
	}
	public void setTxtID(String txtID) {
		this.txtID = txtID;
	}
	public String getTxtTitle() {
		return txtTitle;
	}
	public void setTxtTitle(String txtTitle) {
		this.txtTitle = txtTitle;
	}
	public String getTxtContent() {
		return txtContent;
	}
	public void setTxtContent(String txtContent) {
		this.txtContent = txtContent;
	}
	
}
